# Buscar el máximo en un arreglo con max
# Este programa encuentra el número más grande en un arreglo de números.

# Se define un arreglo de números
arreglo = [10, 25, 8, 17, 30]

# Se encuentra el número máximo en el arreglo
numero_maximo = arreglo.max

# Se imprime el número máximo
puts "El número más grande en el arreglo es: #{numero_maximo}"
