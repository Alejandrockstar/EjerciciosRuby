# Invertir un arreglo
# Este programa invierte los elementos de un arreglo y los imprime.

# Se define un arreglo de ejemplo
arreglo = [1, 2, 3, 4, 5]

# Se invierte el arreglo
arreglo_invertido = arreglo.reverse

# Se imprime el arreglo invertido
puts "El arreglo invertido es: #{arreglo_invertido}"
