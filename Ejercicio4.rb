# Iterador con un arreglo
# Este programa crea un arreglo de frutas y utiliza un iterador para imprimir cada fruta.

# Se define un arreglo con frutas
frutas = ["manzana", "banana", "naranja", "uva", "mango"]

# Se itera sobre el arreglo de frutas
frutas.each do |fruta|
  # Se imprime cada fruta
  puts fruta
end
